package Week1;

public class RelationAndBooleanDemo {
    public static void main(String[] args) {
        int first = 10, second = 20;

        System.out.println(first + " is equal to " + second + " = " + (first == second));
        System.out.println(first + " is not equal to " + second + " = " +(first != second));
        System.out.println(first + " is less than to " + second + " = " +(first < second));
        System.out.println(first + " is less than or equal to " + second + " = " +(first <= second));
        System.out.println(first + " is greater than to " + second + " = " +(first > second));
        System.out.println(first + " is greater than or equal to " + second + " = " +(first >= second));
    }
}

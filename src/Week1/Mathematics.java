package Week1;

public class Mathematics {

    public int addition(int firstValue, int secondValue) {
        return firstValue + secondValue;
    }

    public int subtraction(int firstValue, int secondValue) {
        return firstValue - secondValue;
    }

    public int multiply(int firstValue, int secondValue) {
        return firstValue * secondValue;
    }

    public int divide(int firstValue, int secondValue) {
        return firstValue / secondValue;
    }
}

